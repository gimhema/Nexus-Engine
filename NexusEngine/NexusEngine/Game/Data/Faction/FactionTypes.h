#pragma once

#include <cstdint>

// ─────────────────────────────────────────────────────────────────────────────
// EFactionId — 진영 식별자
//
// 플레이어·NPC·몬스터 모두 하나의 진영에 속한다.
// 두 엔티티의 관계(우호/중립/적대)는 FactionRegistry에서 진영 쌍으로 조회한다.
// 어떤 진영이든 FactionRegistry에 등록한 관계에 따라 적대/우호가 결정되므로
// 숫자 범위에 특별한 의미는 없다. 아래 값들은 예시 진영이다.
// ─────────────────────────────────────────────────────────────────────────────
enum class EFactionId : uint32_t
{
    NONE             = 0,

    ALLIANCE         = 1,
    HORDE            = 2,

    STORMWIND        = 100,
    IRONFORGE        = 101,

    UNDEAD_SCOURGE   = 200,
    CREATURE_WILD    = 201,

    NEUTRAL_MERCHANT = 300,

    USER_DEFINED     = 1000,
};

// ─────────────────────────────────────────────────────────────────────────────
// EFactionRelation — 두 진영 간 관계
//
// FactionRegistry에서 (A, B) 쌍으로 조회.
// 미등록 쌍의 기본값: NEUTRAL.
// ─────────────────────────────────────────────────────────────────────────────
enum class EFactionRelation : uint8_t
{
    HOSTILE  = 0,   // 적대 — 공격 가능
    NEUTRAL  = 1,   // 중립 — 무시 (공격받으면 반격)
    FRIENDLY = 2,   // 우호 — 공격 불가
};

// ─────────────────────────────────────────────────────────────────────────────
// EAIType — 엔티티 전투 AI 행동 유형
//
// NPC·몬스터 공통 속성. EntityBase가 보유.
// ─────────────────────────────────────────────────────────────────────────────
enum class EAIType : uint8_t
{
    PASSIVE    = 0,   // 공격받아도 반격 없음 (예: 토끼, 상인)
    DEFENSIVE  = 1,   // 공격받으면 반격, 먼저 공격하지 않음 (예: 경비병)
    AGGRESSIVE = 2,   // 어그로 범위 내 적대 진영 발견 시 먼저 공격 (예: 슬라임)
};
